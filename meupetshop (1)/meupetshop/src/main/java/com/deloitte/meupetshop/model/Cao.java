package com.deloitte.meupetshop.model;

public class Cao {

    public String chip;
    public String nome;
    public int idade;
    public String cpfDono;

    public Cao(){}

    public Cao(String chip, String nome, int idade, String cpfDono) {
        this.chip = chip;
        this.nome = nome;
        this.idade = idade;
        this.cpfDono = cpfDono;
    }

}